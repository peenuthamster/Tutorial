package Calculator;

public class Calculator {

    public double calculateAddition(double numb1, double numb2) {
        return numb1 + numb2;
    }
    public double calculateSubtraction(double numb1, double numb2) {
        return Math.abs(numb1 - numb2);
    }
    public double calculateMultiplication(double numb1, double numb2) {
        return numb1 * numb2;
    }
    public double calculateDivision(double numb1, double numb2) {
        return numb1 / numb2;
    }
}
